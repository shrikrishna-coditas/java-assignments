public class IfCommand {
    public static void main(String args[]){
        int m=2,x=0;
        if(m==0){
            x=x+2;
            System.out.println("X="+x);
        }
        else if(m==1){
            x=x+4;
            System.out.println("X="+x);
        }
        else if(m==2){
            x=x+6;
            System.out.println("X="+x);
        }

    }
}
