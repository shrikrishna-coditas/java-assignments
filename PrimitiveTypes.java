public class PrimitiveTypes {
    public static void main(String[] args){
        byte byte_a=120,byte_b=90;
        System.out.println(byte_a+byte_b);

        short short_a=22,short_b=48;

        System.out.println(short_a+short_b);

        int int_a=7,int_b=98;
        System.out.println(int_a+int_b);

        long long_a=45456452,long_b=654688989;
        System.out.println(long_a+long_b);

        float float_a=20.5f,float_b=55.7f;
        System.out.println(float_a+float_b);

        double double_a=44.343,double_b=55.879;
        System.out.println(double_a+double_b);

        boolean boolean_a=true,boolean_b=false;//we cannot add boolean variables

        char char_a='a',char_b='B';
        System.out.println(char_a+char_b);

    }
}
